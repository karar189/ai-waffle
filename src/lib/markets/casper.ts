function emptyBuckets(): CasperMarketBuckets<CasperMarketRow> {
  return {
    spot: [],
    linear_perp: [],
    inverse_perp: [],
  };
}

function totalsFromRows(rows: CasperMarketRow[]): {
  pairCount: number;
  totalVolume24h: number;
  totalOiUsd: number | null;
} {
  const totalVolume24h = rows.reduce((sum, row) => sum + (row.volume24h ?? 0), 0);
  const oiRows = rows
    .map((row) => row.oiUsd)
    .filter((value): value is number => value !== null);

  return {
    pairCount: rows.length,
    totalVolume24h,
    totalOiUsd: oiRows.length ? oiRows.reduce((sum, value) => sum + value, 0) : null,
  };
}

