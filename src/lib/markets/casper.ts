export async function loadCasperMarkets(): Promise<CasperMarketsResponse> {
  const cex: CasperMarketBuckets<CasperMarketRow> = {
    spot: STATIC_CEX_SPOT_ROWS,
    linear_perp: STATIC_CEX_LINEAR_PERP_ROWS,
    inverse_perp: [],
  };

  const dex = emptyBuckets();

  const cexTotals: CasperMarketTotalsBuckets = {
    spot: totalsFromRows(cex.spot),
    linear_perp: totalsFromRows(cex.linear_perp),
    inverse_perp: totalsFromRows(cex.inverse_perp),
  };

  const dexTotals: CasperMarketTotalsBuckets = {
    spot: totalsFromRows(dex.spot),
    linear_perp: totalsFromRows(dex.linear_perp),
    inverse_perp: totalsFromRows(dex.inverse_perp),
  };

  const exchangeCount = new Set(
    [...cex.spot, ...cex.linear_perp].map((row) => `${row.venueKind}:${row.exchange}`)
  ).size;

  return {
    fetchedAt: "2026-07-08T02:04:37+05:30",
    network: "casper-mainnet",
    source: "Static Snapshot",
    tokenSymbol: "CSPR",
    exchangeCount,
    marketCount: cex.spot.length + cex.linear_perp.length,
    totalVolume24h:
      cexTotals.spot.totalVolume24h +
      cexTotals.linear_perp.totalVolume24h +
      cexTotals.inverse_perp.totalVolume24h,
    totalOiUsd: cexTotals.linear_perp.totalOiUsd,
    cex,
    dex,
    totals: {
      cex: cexTotals,
      dex: dexTotals,
    },
  };
}
